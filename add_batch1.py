# ターゲット分布: A:13, B:12, C:13, D:12
TARGET = ['A']*13 + ['B']*12 + ['C']*13 + ['D']*12
random.seed(42)
random.shuffle(TARGET)

NEW_QUESTIONS = []
for idx, (qid, question, correct, w1, w2, w3, explanation, stars) in enumerate(RAW_QUESTIONS):
    target_answer = TARGET[idx]
    # choices を並び替えて target_answer 位置に correct を配置
    choices_list = [w1, w2, w3]
    random.shuffle(choices_list)
    all_choices = {'A': None, 'B': None, 'C': None, 'D': None}
    positions = ['A', 'B', 'C', 'D']
    positions.remove(target_answer)
    all_choices[target_answer] = correct
    for i, pos in enumerate(positions):
        all_choices[pos] = choices_list[i]
    NEW_QUESTIONS.append({
        "id": qid,
        "category": "一般",
        "field": "基礎看護",
        "question": question,
        "choices": all_choices,
        "answer": target_answer,
        "explanation": explanation,
        "stars": stars
    })

# 分布確認
dist = {}
for q in NEW_QUESTIONS:
    dist[q['answer']] = dist.get(q['answer'], 0) + 1
print(f"新規問題数: {len(NEW_QUESTIONS)}, ID: {NEW_QUESTIONS[0]['id']}〜{NEW_QUESTIONS[-1]['id']}")
