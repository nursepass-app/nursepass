# Build questions with balanced answer distribution
TARGET = ['A']*13 + ['B']*12 + ['C']*13 + ['D']*12
random.seed(SEED)
random.shuffle(TARGET)

FIELDS = (
    ["周手術期"] * 15 +
    ["救急・クリティカル"] * 15 +
    ["小児看護"] * 10 +
    ["母性看護"] * 10
)

NEW_QUESTIONS = []
for idx, (question, correct, w1, w2, w3, explanation, stars) in enumerate(RAW_QUESTIONS):
    qid = START_ID + idx
    target_answer = TARGET[idx]
    choices_list = [w1, w2, w3]
    random.seed(SEED + idx)
    random.shuffle(choices_list)
    choices = {}
    wrong_idx = 0
    for opt in ['A', 'B', 'C', 'D']:
        if opt == target_answer:
            choices[opt] = correct
        else:
            choices[opt] = choices_list[wrong_idx]
            wrong_idx += 1
    NEW_QUESTIONS.append({
        "id": qid,
        "category": "一般",
        "field": FIELDS[idx],
        "question": question,
        "choices": choices,
        "answer": target_answer,
        "explanation": explanation,
        "stars": stars
    })

